import { chromium } from 'playwright'
import { writeFileSync, readFileSync } from 'fs'

const UA = 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) Chrome/120.0 Safari/537.36'
const sleep = (ms) => new Promise((r) => setTimeout(r, ms))

// ---------- 1. Scrape /all for the community collection ----------
const browser = await chromium.launch()
const page = await browser.newPage({ userAgent: UA })

console.log('Loading bekasiboardgame.my.id/all ...')
// Firestore keeps the socket open so networkidle never fires — use domcontentloaded + wait for rows
await page.goto('https://bekasiboardgame.my.id/all', { waitUntil: 'domcontentloaded', timeout: 60000 })
await page.waitForSelector('.game_row', { timeout: 30000 })
await page.waitForTimeout(3000)

const scraped = await page.evaluate(() => {
  const rows = Array.from(document.querySelectorAll('.game_row'))
  return rows.map((row) => {
    const link = row.querySelector('.game_name a')
    const href = link?.getAttribute('href') || ''
    const idMatch = href.match(/boardgame\/(\d+)/)
    const cats = Array.from(row.querySelectorAll('.game_category .cat_pills')).map((c) => c.textContent.trim())
    const owners = Array.from(row.querySelectorAll('.game_own .user_pill span')).map((s) => s.textContent.trim())
    const ratingText = row.querySelector('.game_rating')?.textContent?.trim() || ''
    const playersText = row.querySelector('.game_players')?.textContent?.trim() || ''
    const playtimeText = row.querySelector('.game_playtime')?.textContent?.trim() || ''
    return {
      id: idMatch ? idMatch[1] : '',
      name: link?.textContent?.trim() || '',
      categories: cats,
      owners,
      ratingText,
      playersText,
      playtimeText,
    }
  })
})

await browser.close()
const games = scraped.filter((g) => g.id && g.name)
console.log(`Scraped ${games.length} community games`)

// Build member list from owners
const memberSet = new Set()
games.forEach((g) => g.owners.forEach((o) => memberSet.add(o)))
const members = Array.from(memberSet).sort()
console.log(`Members (${members.length}): ${members.join(', ')}`)

// ---------- 2. Enrich each game with high-res image + accurate stats ----------
const parseNum = (s) => {
  const m = (s || '').match(/[\d.]+/)
  return m ? parseFloat(m[0]) : null
}

const enriched = []
for (let i = 0; i < games.length; i++) {
  const g = games[i]
  let detail = {}
  try {
    const res = await fetch(`https://api.geekdo.com/api/geekitems?objectid=${g.id}&objecttype=thing`, {
      headers: { 'User-Agent': UA, Accept: 'application/json' },
    })
    if (res.ok) detail = (await res.json()).item || {}
  } catch (e) {
    console.warn(`  ${g.name}: ${e.message}`)
  }

  // High-res image: prefer original, fall back through larger sizes
  const img = detail.images || {}
  const hiRes = img.original || img.previewthumb || img.square200 || img.thumb || ''
  const thumb = img.previewthumb || img.square200 || img.thumb || hiRes

  enriched.push({
    id: String(g.id),
    name: g.name,
    yearPublished: detail.yearpublished ? parseInt(detail.yearpublished, 10) : null,
    image: hiRes,
    thumbnail: thumb,
    minPlayers: detail.minplayers ? parseInt(detail.minplayers, 10) : (parseNum(g.playersText) || 1),
    maxPlayers: detail.maxplayers ? parseInt(detail.maxplayers, 10) : (parseNum(g.playersText) || 1),
    minPlayTime: detail.minplaytime ? parseInt(detail.minplaytime, 10) : (parseNum(g.playtimeText) || 0),
    maxPlayTime: detail.maxplaytime ? parseInt(detail.maxplaytime, 10) : (parseNum(g.playtimeText) || 0),
    communityRating: parseNum(g.ratingText) ?? 0,
    userRating: null,
    bggRank: null,
    numPlays: 0,
    categories: g.categories,
    owners: g.owners,
  })
  if ((i + 1) % 20 === 0 || i === games.length - 1) console.log(`  enriched ${i + 1}/${games.length}`)
  await sleep(200)
}

// ---------- 3. Safety guard: never overwrite good data with a broken scrape ----------
// If the source site changes or a fetch fails wholesale, refuse to write so a
// scheduled run can't silently commit an empty/partial collection.
const MIN_GAMES = 50
if (enriched.length < MIN_GAMES) {
  console.error(`\nAborting: scraped only ${enriched.length} games (< ${MIN_GAMES}). Not writing lib/community.ts.`)
  process.exit(1)
}
try {
  const existing = readFileSync('lib/community.ts', 'utf8')
  const prev = Number(existing.match(/\/\/ (\d+) games owned/)?.[1])
  if (prev && enriched.length < prev * 0.75) {
    console.error(`\nAborting: scraped ${enriched.length} games vs ${prev} previously (>25% drop). Not writing.`)
    process.exit(1)
  }
} catch {
  /* no existing file — fine */
}

// ---------- 4. Write the data file ----------
const file = `import { CommunityGame } from './types'

// Real Barudak Board Game Club collection — scraped from bekasiboardgame.my.id
// ${enriched.length} games owned across ${members.length} members.
// Generated by scripts/scrape-community.mjs — do not edit by hand.

export const MEMBERS: string[] = ${JSON.stringify(members)}

export const COMMUNITY_GAMES: CommunityGame[] = ${JSON.stringify(enriched, null, 2)}
`
writeFileSync('lib/community.ts', file)
console.log(`\nWrote lib/community.ts (${enriched.length} games, ${members.length} members)`)
